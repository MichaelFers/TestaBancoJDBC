
public class Usuario {
	
	private String nome;
	private String anos;
	
	public Usuario(String nome, String anos) {
		this.nome = nome;
		this.anos = anos;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getAnos() {
		return anos;
	}

	public void setAnos(String anos) {
		this.anos = anos;
	}
	
	

}
